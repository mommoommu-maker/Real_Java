package day01;

public class Ex01_Print {
	public static void main(String[] args) {
		//sysout + ctrl + space하면 sysout 자동완성 된다!
		//main + ctrl + space하면 main 자동완성 된다!
		System.out.print("Hello");
		System.out.println("출력 후 Hello");
		System.out.println("이건 줄바꿈");
		//shift + enter = 바로 엔터
		System.out.println();
		System.out.println("이건 이어지는 문자열");
	}
}
